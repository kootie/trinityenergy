<script>
    function initMap() {
        const myLatLng = {
            lat: -1.2973926,
            lng: 36.7927182
        };

        const map = new google.maps.Map(document.getElementById("map"), {
            zoom: 8,
            center: myLatLng,
        });

        var locations = {{ Js::from($mapData) }};

        var infowindow = new google.maps.InfoWindow();

        var marker, i;

        for (i = 0; i < locations.length; i++) {
            marker = new google.maps.Marker({
                position: new google.maps.LatLng(locations[i].lat, locations[i].lng),
                map: map
            });

            google.maps.event.addListener(marker, 'click', (function(marker, i) {
                return function() {
                    var lat = locations[i].lat;
                    var lng = locations[i].lng;
                    infowindow.setContent('<div>' +
                        '<h5>' + locations[i].project_name + '</h5>' +
                        '<p>Phone Number: ' + locations[i].primary_phone + '</p>' +
                        '<p>Location: ' + locations[i].project_location + '</p>' +
                        '<button id="getDirections" onclick="getDirections(' + lat + ',' +
                        lng + ')">Get Directions</button>' +
                        '</div>');
                    infowindow.open(map, marker);
                }
            })(marker, i));

        }
    }

    // Function to get directions to the clicked marker
    function getDirections(lat, lng) {
        var directionsUrl = 'https://www.google.com/maps/dir//' +
            encodeURIComponent(latitude + ',' + lng);
        window.open(directionsUrl);
    }



    window.initMap = initMap;
</script>

